'''*******************************************************************************************************
Given an array of integers, write a function to find the median of the array.

Example:
Input: [1, 7, 3, 6, 9, 15, 0, 2]
Output: [2, 0, 15, 9, 6, 3, 7, 1]
********************************************************************************************************'''




def median(arr):
    n = len(arr)
    arr = sorted(arr)
    if n % 2 == 0:
        return (arr[n//2-1] + arr[n//2]) / 2
    else:
        return arr[n//2]


print(median([1, 7, 3, 6, 9, 15, 0, 2]))







