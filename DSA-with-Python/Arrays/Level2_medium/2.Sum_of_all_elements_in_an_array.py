'''*******************************************************************************************************
sum of all elements in an array :
Find the sum of all elements in an array.

Example:
Input: [1, 7, 3, 6, 9, 15, 0, 2]
Output: 0
***********************************************************************************************************'''

def sum(arr):
    sum = 0 
    for i in range(0, len(arr)):
        sum = sum + arr[i]
    print("The sum of the array is :- ",sum)
arr = [1, 7, 3, 6, 9, 15, 0, 2]
sum(arr)

